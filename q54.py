# Q54: 奖金提成
def bonus(i):
    if i <= 10:
        b = i * 0.1
    elif i <= 20:
        b = 10 * 0.1 + (i-10) * 0.075
    elif i <= 40:
        b = 10 * 0.1 + 10 * 0.075 + (i-20) * 0.05
    elif i <= 60:
        b = 10 * 0.1 + 10 * 0.075 + 20 * 0.05 + (i-40) * 0.03
    elif i <= 100:
        b = 10 * 0.1 + 10 * 0.075 + 20 * 0.05 + 20 * 0.03 + (i-60) * 0.015
    else:
        b = 10 * 0.1 + 10 * 0.075 + 20 * 0.05 + 20 * 0.03 + 40 * 0.015 + (i-100) * 0.01
    return b
print(bonus(189))
