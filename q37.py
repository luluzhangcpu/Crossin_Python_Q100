# Q37: 矩阵计算
# 【分析】创建一个新的 3 行 3 列的矩阵，使用 for 迭代并取出 X 和 Y 矩阵中对应位置的值，相加后放到新矩阵的对应位置中。
 
# 参考示例
# 输入：
# X = [[1,2,3],
#      [4,5,6],
#      [7,8,9]]

# Y = [[1,2,3],
#      [4,5,6],
#      [7,8,9]]

# 输出：
# Z = [[2,4,6],
#      [8,10,12],
#      [14,16,18]]

import numpy as np

X = np.array([[12,7,3],[4,5,6],[7,8,9]])
Y = np.array([[5,8,1],[6,7,3],[4,5,9]])
print(X + Y)
